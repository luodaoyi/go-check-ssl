function normalizeLocale(locale?: string | null): Locale {
  const normalized = locale?.trim().toLowerCase();
  switch (normalized) {
    case "zh":
    case "zh-cn":
    case "zh-sg":
      return "zh-CN";
    case "zh-tw":
    case "zh-hk":
    case "zh-mo":
      return "zh-TW";
    default:
      break;
  }

  if (normalized?.startsWith("zh-hans")) return "zh-CN";
  if (normalized?.startsWith("zh-hant")) return "zh-TW";
  if (normalized?.startsWith("fr")) return "fr";
  if (normalized?.startsWith("de")) return "de";
  if (normalized?.startsWith("ru")) return "ru";
  if (normalized?.startsWith("ar")) return "ar";
  if (normalized?.startsWith("pt")) return "pt";
  if (normalized?.startsWith("ja")) return "ja";
  if (normalized?.startsWith("ko")) return "ko";
  if (normalized?.startsWith("hi")) return "hi";
  if (normalized?.startsWith("it")) return "it";
  if (normalized?.startsWith("es")) return "es";
  return "en";
}

function interpolate(template: string, params?: Params) {
  if (!params) return template;
  return Object.entries(params).reduce((result, [key, value]) => {
    return result.replaceAll(`{{${key}}}`, String(value));
  }, template);
}

function detectLocale(): Locale {
  if (typeof window !== "undefined") {
    const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY);
    if (stored) {
      return normalizeLocale(stored);
    }
  }

  if (typeof navigator !== "undefined") {
    const candidates = navigator.languages?.length ? navigator.languages : [navigator.language];
    for (const candidate of candidates) {
      return normalizeLocale(candidate);
    }
  }

  return "en";
}

interface I18nContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, params?: Params) => string;
  formatDateTime: (value?: string | null) => string;
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => detectLocale());

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
    }
    document.documentElement.lang = locale;
    document.documentElement.dir = locale === "ar" ? "rtl" : "ltr";
  }, [locale]);

  const t = useCallback((key: TranslationKey, params?: Params) => {
    const template = messages[locale][key] ?? enMessages[key] ?? key;
    return interpolate(template, params);
  }, [locale]);

  const formatDateTime = useCallback((value?: string | null) => {
    if (!value) {
      return t("common.none");
    }

    const date = new Date(value);
    if (Number.isNaN(date.getTime())) {
      return t("common.none");
    }

    return new Intl.DateTimeFormat(locale, {
      dateStyle: "medium",
      timeStyle: "short",
    }).format(date);
  }, [locale, t]);

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    setLocale,
    t,
    formatDateTime,
  }), [formatDateTime, locale, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return value;
}
